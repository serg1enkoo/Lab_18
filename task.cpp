// Підключення стандартної бібліотеки iostream для роботи з ввідними/вивідними потоками,
// що дозволяє використовувати об'єкти cout для виведення даних в консоль.
#include <iostream>

// Використання стандартного простору імен std, щоб не писати "std::" перед кожним елементом стандартної бібліотеки.
using namespace std;

// -------------------------------------------------------------
// Оголошення та визначення перевантажених функцій increment.
// Функції працюють з змінними типу int, які передаються за посиланням,
// що дозволяє безпосередньо змінювати значення переданої змінної.

// 1. Перша перевантажена версія функції increment, яка приймає один аргумент:
//    змінну int за посиланням. При виклику ця функція збільшує значення змінної на 1.
void increment(int &var) { // & - значить воно бере змінну яку передають і змінює її ЙОУ!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
    // var += 1; – оператор += додає до змінної значення праворуч (1) і зберігає результат у змінній var.
    // Це еквівалентно запису var = var + 1.
    var += 1;
}

// 2. Друга перевантажена версія функції increment, яка приймає два аргументи:
//    - перший аргумент: змінна int за посиланням (var), яку потрібно змінити,
//    - другий аргумент: ціле число (value), яке задає величину приросту.
//    Функція збільшує значення змінної var на величину, задану value.
void increment(int &var, int value) {
    // var += value; – додає значення змінної value до змінної var.
    var += value;
}
// -------------------------------------------------------------

// Функція main є точкою входу в програму. Тут починається виконання програми.
int main(void) {
    // Оголошення змінної var типу int і її ініціалізація нульовим значенням.
    int var = 0;
    
    // Цикл for, який виконується 10 разів, перебираючи значення змінної i від 0 до 9.
    for (int i = 0; i < 10; i++) {
        // Використовуємо умовний оператор if для перевірки значення i:
        // якщо i непарне (тобто остача від ділення i на 2 не дорівнює 0),
        // то викликається функція increment з одним аргументом.
        if (i % 2)
            // Виклик функції increment, яка збільшує змінну var на 1.
            increment(var);
        else
            // Якщо i парне (тобто i % 2 == 0), викликається перевантажена функція increment,
            // що приймає два аргументи: змінну var та значення i. Таким чином, var збільшується на i.
            increment(var, i);
    }
    
    // Після завершення циклу, виводимо кінцеве значення змінної var.
    // Згідно з умовою задачі, очікуване значення має бути 25.
    cout << var << endl;
    
    // Повертаємо 0 для позначення успішного завершення роботи програми.
    return 0;
}
